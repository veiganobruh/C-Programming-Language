#include <stdio.h>
#include <stdlib.h>


int main()
{
    unsigned int i=1;
    int soma = 0;
    while(i<1000)
    {
        if ( ((i%3)==0)||((i%5)==0))
        {
            soma = soma + i ;
        }
        i++;
    }
    printf("\n\n Soma: %d", soma);
    return 0;
}
